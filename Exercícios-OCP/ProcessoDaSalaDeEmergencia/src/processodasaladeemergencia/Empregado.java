package processodasaladeemergencia;

public class Empregado {
    int id;
    String nome;
    String departamento;
    boolean trabalhando;

    public Empregado(int id, String nome, String departamento, boolean trabalhando) {
        this.id = id;
        this.nome = nome;
        this.departamento = departamento;
        this.trabalhando = trabalhando;
    }
}
